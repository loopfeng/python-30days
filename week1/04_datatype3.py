# Số phức
#complex(<phần thực>, <phần ảo>)
#tên biến = <phần thực> + <phần ảo>j
#Complex là số phức
#<tên_biến>.real
#<tên_biến>.imag
c = complex(2,5)
print(c.real)# lấy ra phần thực
print(c.imag)# lấy ra phần ảo
# chia luôn kết quả ra số thực
c = 10/3
print(c)
# chia lấy số nguyên
c = 10//3
print(c)
# chia lấy dư
c = 10%3
print(c)
# lũy thừa x cơ số y
c = 2**2
print(c)