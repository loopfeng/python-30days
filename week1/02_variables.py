#Day 2 - Biến và Kiểu dữ liệu
a = 10
b = 3.14
c = "Hello"
print(a)  # In ra giá trị của biến a
print(b)  # In ra giá trị của biến b
print(c)  # In ra giá trị của biến c
# Kiểm tra kiểu dữ liệu của các biến
print(type(a))  # Kiểu dữ liệu của a là int
print(type(b))  # Kiểu dữ liệu của b là float
print(type(c))  # Kiểu dữ liệu của c là str
# Thay đổi giá trị của biến a
a = 20
print(a)  # In ra giá trị mới của biến a
# Kết hợp các biến để tạo ra một chuỗi mới
greeting = c + " World!"
print(greeting)  # In ra chuỗi mới
# Sử dụng biến trong phép toán
result = a + b
print(result)  # In ra kết quả của phép toán

