import math  # Nhập thư viện math để dùng các hàm toán học

print(math.trunc(3.9))   # trunc(): cắt bỏ phần thập phân, chỉ lấy phần nguyên -> 3

print(math.fabs(-3))     # fabs(): lấy giá trị tuyệt đối dạng số thực -> 3.0

print(math.sqrt(16))     # sqrt(): tính căn bậc hai -> 4.0

print(math.gcd(6,4))     # gcd(): tìm ước chung lớn nhất của 6 và 4 -> 2